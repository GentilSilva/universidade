package lista7;

public class Atividade1 {

	public static void main(String[] args) {
		
		System.out.println("Numeros de 1 a 100 de forma crescente");
		for(int i = 1; i <= 100; i++) {
			System.out.println(i);
		}
		
		System.out.println("Numeros de 100 a 1 de forma decrescente");
		for(int i = 100; i > 0; i--) {
			System.out.println(i);
		}
		
	}
	
}
